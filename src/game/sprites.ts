// ===== DRAW =====
export function drawSprite(
  ctx: CanvasRenderingContext2D, sprite: SpriteData,
  frameIndex: number, x: number, y: number,
  scale: number = 1, flipX: boolean = false,
) {
  const frame = sprite.frames[frameIndex % sprite.frames.length];
  for (let row = 0; row < sprite.height; row++) {
    for (let col = 0; col < sprite.width; col++) {
      const idx = frame[row][col];
      if (idx === 0) continue;
      ctx.fillStyle = sprite.palette[idx];
      const dc = flipX ? (sprite.width - 1 - col) : col;
      ctx.fillRect(Math.floor(x + dc * scale), Math.floor(y + row * scale), scale, scale);
    }
  }
}
